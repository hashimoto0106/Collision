import numpy as np


a = np.array([[1,2],[2,4],[3,6]])
#---------------------------
# array([[1, 2],
#        [2, 4],
#        [3, 6]])
#---------------------------


# 標本分散（bias=1）

matrix = np.cov(a, rowvar=0, bias=1)
print(matrix)
#-------------------------------------
# array([[ 0.66666667,  1.33333333],
#        [ 1.33333333,  2.66666667]])
#-------------------------------------
